package ddit_project.vo;

public class LunchVO {
	private String lunch_id;
	private String lunch_name;
	private String lunch_date;
	private String lunch_check;
	public String getLunch_id() {
		return lunch_id;
	}
	public void setLunch_id(String lunch_id) {
		this.lunch_id = lunch_id;
	}
	public String getLunch_check() {
		return lunch_check;
	}
	public void setLunch_check(String lunch_check) {
		this.lunch_check = lunch_check;
	}
	public String getLunch_name() {
		return lunch_name;
	}
	public void setLunch_name(String lunch_name) {
		this.lunch_name = lunch_name;
	}
	public String getLunch_date() {
		return lunch_date;
	}
	public void setLunch_date(String lunch_date) {
		this.lunch_date = lunch_date;
	}
	
}
